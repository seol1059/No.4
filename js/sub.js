$(function () {
    if (window.matchMedia("(max-width:599px) ").matches) {

        $(window).scroll(function () {

            var ws = $(this).scrollTop();



            if (ws > 400) {

                $('.con1').css("transform", "translateY(0px)").css("opacity", "1");


            }

            if (ws > 500) {

                $('.st1').css("transform", "translateY(0px)").css("opacity", "1");
                $('.st2').css("transform", "translateY(0px)").css("opacity", "1");
                $('.st3').css("transform", "translateY(0px)").css("opacity", "1");
                $('.st4').css("transform", "translateY(0px)").css("opacity", "1");

            }

            if (ws > 1000) {

                $('.div1 > .textbox:nth-of-type(1)').css("transform", "translateY(0px)").css("opacity", "1");
                $('.div1 > .textbox:nth-of-type(2)').css("transform", "translateY(0px)").css("opacity", "1");
                $('.div1 > .textbox:nth-of-type(3)').css("transform", "translateY(0px)").css("opacity", "1");


            }
            if (ws > 1100) {

                $('.div1 > .textbox:nth-of-type(4)').css("transform", "translateY(0px)").css("opacity", "1");
                $('.div1 > .textbox:nth-of-type(5)').css("transform", "translateY(0px)").css("opacity", "1");
                $('.div1 > .textbox:nth-of-type(6)').css("transform", "translateY(0px)").css("opacity", "1");


            }

            if (ws > 1200) {

                $('.div1 > .textbox:nth-of-type(7)').css("transform", "translateY(0px)").css("opacity", "1");
                $('.div1 > .textbox:nth-of-type(8)').css("transform", "translateY(0px)").css("opacity", "1");
                $('.div1 > .textbox:nth-of-type(9)').css("transform", "translateY(0px)").css("opacity", "1");


            }


        });

    } else {

        /* 뷰포트 너비가 400 픽셀 미만 */

    }
});


















